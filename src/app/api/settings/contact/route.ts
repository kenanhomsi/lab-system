import { NextResponse } from "next/server";
import { MOCK_CONTACT_SETTINGS } from "@/lib/api/bff-public-mock-data";

export async function GET() {
  const backendUrl = process.env.BACKEND_URL || "http://localhost:4000";
  const isUpstreamReady = process.env.UPSTREAM_BACKEND_READY === "true";

  if (isUpstreamReady) {
    try {
      const res = await fetch(`${backendUrl}/settings/contact`, { cache: "no-store" });
      if (!res.ok) {
        return NextResponse.json(
          { error: "Upstream request failed" },
          { status: res.status },
        );
      }
      return NextResponse.json(await res.json());
    } catch {
      return NextResponse.json({ error: "Upstream unavailable" }, { status: 502 });
    }
  }

  return NextResponse.json(MOCK_CONTACT_SETTINGS);
}
